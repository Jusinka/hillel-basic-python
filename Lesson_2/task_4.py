# Дано натуральне число. Потрібно визначити, чи є рік із цим числом високосним. Якщо рік є
# високосним, то виведіть `YES`, інакше виведіть `NO`. Нагадаємо, що відповідно до григоріанського календаря, рік є
# високосним, якщо його номер кратний 4, але не кратний 100, а також якщо він кратний 400.
year = int(input("Введіть рік: "))

if (year % 4 == 0 and year % 100 != 0) or year % 400 == 0:
    print("YES")
else:
    print("NO")